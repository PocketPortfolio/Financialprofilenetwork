#!/usr/bin/env node
/**
 * Bake unique 16:9 landing plates from seed deck art — one distinct frame per section.
 * SSOT: docs/seed/open-portfolio-web-sota-brief.md
 *
 * Each output PNG is physically cropped (not CSS object-position reuse).
 * Run: npm run sync:open-web-plates
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { bakeFullPlate16x9, bakePlate16x9, fileHashPrefix } from './lib/plate-crop.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const DECK_DIR = path.join(ROOT, 'docs', 'seed', 'design-plates');
const WEB_DIR = path.join(ROOT, 'public', 'open', 'landing', 'plates');

/** One row per landing slot — unique baked output, headline-aligned source crop. */
const PLATE_MAP = [
  {
    dest: 'web-hero-glass-vault.png',
    src: 'slide-01-frontier.png',
    region: { left: 0.52, top: 0, width: 0.48, height: 1 },
    label: 'hero — client-edge glass cubes (sovereign perimeter)',
  },
  {
    dest: 'web-boundary-split-brain.png',
    src: 'slide-02-split-brain.png',
    region: { left: 0, top: 0, width: 0.58, height: 1 },
    label: 'subHero — ingest + client node (architecture moat)',
  },
  {
    dest: 'web-boundary-frontier.png',
    src: 'slide-01-frontier.png',
    region: { left: 0, top: 0, width: 0.48, height: 1 },
    label: 'threat — legacy cloud monolith (regulatory exposure)',
  },
  {
    dest: 'web-traction-dual-pane.png',
    src: 'slide-03-traction.png',
    full: true,
    label: 'bridge — full dual-pane (npm + global map, contain fit)',
  },
  {
    dest: 'web-traction-heatmap.png',
    src: 'slide-03-traction.png',
    region: { left: 0.5, top: 0, width: 0.5, height: 1 },
    label: 'tracks — MAU heatmap pane + firefly map overlay',
  },
  {
    dest: 'web-split-brain-pillars.png',
    src: 'slide-02-split-brain.png',
    region: { left: 0.48, top: 0, width: 0.52, height: 1 },
    label: 'pillars — stateless server + dissipation (three guarantees)',
  },
  {
    dest: 'web-substrate-matrix.png',
    src: 'slide-05-control-panel.png',
    region: { left: 0.04, top: 0.02, width: 0.92, height: 0.84 },
    label: 'packages — FIN control matrix grid (interim until 3.2 net-new wafer)',
  },
  {
    dest: 'web-clean-room-console.png',
    src: 'slide-05-control-panel.png',
    full: true,
    label: 'contact — FIN glass briefing console (executive perimeter)',
  },
];

async function main() {
  fs.mkdirSync(WEB_DIR, { recursive: true });
  const manifest = [];
  const hashes = new Map();

  for (const row of PLATE_MAP) {
    const srcPath = path.join(DECK_DIR, row.src);
    const destPath = path.join(WEB_DIR, row.dest);
    if (!fs.existsSync(srcPath)) {
      console.error(`Missing deck plate: ${srcPath}`);
      process.exitCode = 1;
      continue;
    }
    if (row.full) {
      await bakeFullPlate16x9(srcPath, destPath);
    } else {
      await bakePlate16x9({ srcPath, destPath, region: row.region });
    }
    const hash = await fileHashPrefix(destPath);
    if (hashes.has(hash)) {
      console.error(`DUPLICATE FRAME: ${row.dest} matches ${hashes.get(hash)} (${hash})`);
      process.exitCode = 1;
    } else {
      hashes.set(hash, row.dest);
    }
    manifest.push({ dest: row.dest, src: row.src, hash, label: row.label, region: row.region ?? 'full' });
    console.log(`✓ ${row.dest} ← ${row.src} (${hash})`);
  }

  const manifestPath = path.join(WEB_DIR, 'manifest.json');
  fs.writeFileSync(
    manifestPath,
    JSON.stringify({ syncedAt: new Date().toISOString(), plates: manifest }, null, 2)
  );

  const cacheEntries = manifest.map((p) => `  '${p.dest}': '${p.hash}',`).join('\n');
  const cachePath = path.join(ROOT, 'lib', 'open-landing-plate-cache.ts');
  fs.writeFileSync(
    cachePath,
    `/**
 * Plate cache-bust tokens — auto-generated by npm run sync:open-web-plates
 * Do not edit by hand.
 */
export const OPEN_LANDING_PLATE_CACHE: Record<string, string> = {
${cacheEntries}
};

/** Public plate URL with content hash — busts Next.js image optimizer cache after sync. */
export function plateSrc(filename: string): string {
  const token = OPEN_LANDING_PLATE_CACHE[filename];
  const base = \`/open/landing/plates/\${filename}\`;
  return token ? \`\${base}?v=\${token}\` : base;
}
`
  );
  console.log(`Wrote plate cache → ${cachePath}`);

  console.log(`\nWrote ${manifest.length} unique baked plates → ${WEB_DIR}`);
  if (!process.exitCode) {
    console.log('All 8 landing frames are byte-distinct.');
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
