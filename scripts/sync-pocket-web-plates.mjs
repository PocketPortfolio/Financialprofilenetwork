function resolveDeckDir() {
  fs.mkdirSync(POCKET_DESIGN, { recursive: true });
  for (const row of PLATE_MAP) {
    const pocketPath = path.join(POCKET_DESIGN, row.src);
    if (fs.existsSync(pocketPath)) continue;
    const sharedPath = path.join(SHARED_DESIGN, row.src);
    if (fs.existsSync(sharedPath)) {
      fs.copyFileSync(sharedPath, pocketPath);
      console.log(`  bootstrap: copied ${row.src} → pocket-design-plates/`);
    }
  }
  return POCKET_DESIGN;
}

async function main() {
  console.log('Pocket web landing plate sync…');
  const deckDir = resolveDeckDir();
  fs.mkdirSync(OUT_DIR, { recursive: true });

  const manifest = [];
  const hashes = new Map();

  for (const row of PLATE_MAP) {
    const srcPath = path.join(deckDir, row.src);
    const destPath = path.join(OUT_DIR, row.dest);
    if (!fs.existsSync(srcPath)) {
      console.error(`Missing deck plate: ${srcPath}`);
      process.exitCode = 1;
      continue;
    }
    if (row.full) {
      await bakeFullPlate16x9(srcPath, destPath);
    } else {
      await bakePlate16x9({ srcPath, destPath, region: row.region });
    }
    const hash = await fileHashPrefix(destPath);
    if (hashes.has(hash)) {
      console.error(`DUPLICATE FRAME: ${row.dest} matches ${hashes.get(hash)} (${hash})`);
      process.exitCode = 1;
    } else {
      hashes.set(hash, row.dest);
    }
    manifest.push({ dest: row.dest, src: row.src, hash, label: row.label, region: row.region ?? 'full' });
    console.log(`  ✓ ${row.dest} ← ${row.src} (${hash})`);
  }

  fs.writeFileSync(
    path.join(OUT_DIR, 'manifest.json'),
    JSON.stringify({ syncedAt: new Date().toISOString(), plates: manifest }, null, 2)
  );

  const cacheEntries = manifest.map((p) => `  '${p.dest}': '${p.hash}',`).join('\n');
  fs.writeFileSync(
    CACHE_FILE,
    `/**
 * Auto-generated by scripts/sync-pocket-web-plates.mjs — do not edit by hand.
 * Run: npm run sync:pocket-web-plates
 */

export const POCKET_LANDING_PLATE_CACHE: Record<string, string> = {
${cacheEntries}
};

export function plateSrc(filename: string): string {
  const token = POCKET_LANDING_PLATE_CACHE[filename];
  const base = \`/pocket/landing/plates/\${filename}\`;
  return token ? \`\${base}?v=\${token}\` : base;
}
`
  );

  console.log(`\nWrote ${manifest.length} unique baked plates → ${OUT_DIR}`);
  if (!process.exitCode) {
    console.log('All 11 landing frames are byte-distinct.');
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
